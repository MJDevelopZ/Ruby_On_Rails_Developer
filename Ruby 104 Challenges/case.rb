weather = 'sunny'
case weather
when 'sunny'
	puts "Grab your sunnies"
when 'windy'
	puts "Grab a scarf"
when 'rainy'
	puts "Grab your umbrella"
when 'foggy'
	puts "Grab your flashlight"
when 'snowy'
	puts "Do you wanna build a snowman?"
when 'icey'
	puts "It doesn't have to be a snowman."
else
	puts "Never ever forget your sunscreen regardless of the weather!"
end